# to break the line use \n
phraseA = "i\nlove\nleague\nof\nlegends"
print(phraseA)

# to make a text in columns use \t
columns = 'league\tof\tlegends'
print(columns)

# to use an inverted bar, write \\ instead of \
directory = 'C:\\Users\\tiomi'

# to use quotations dots in a string use \ before the quotations
phraseB = '\'tititi\' its meeee hahahaha'
print(phraseB)

# an example of tabulation:
data = 'name:\tdavi banni\nage:\t20\ncity:\trio de janeiro'
print(data)
'''
name:	davi banni
age:	20
city:	rio de janeiro
'''